<script>
(() => {
  // filtro
  const chips = document.querySelectorAll('.ind-chip');
  const cards = document.querySelectorAll('.ind-card');
  const params = new URLSearchParams(location.search);
  const cat = params.get('cat');
  if (cat) {
    const btn = Array.from(chips).find(b => b.dataset.filter === cat);
    if (btn) btn.click();
  }
  chips.forEach(ch => ch.addEventListener('click', () => {
    chips.forEach(c=>c.classList.remove('is-active'));
    ch.classList.add('is-active');
    const f = ch.dataset.filter;
    cards.forEach(card => {
      const ok = f === 'all' || card.dataset.cat.includes(f);
      card.style.display = ok ? '' : 'none';
    });
  }));

  // glow follow
  document.querySelectorAll('.ind-card').forEach(card=>{
    card.addEventListener('pointermove', e=>{
      const r = card.getBoundingClientRect();
      card.style.setProperty('--mx', ((e.clientX-r.left)/r.width*100)+'%');
      card.style.setProperty('--my', ((e.clientY-r.top)/r.height*100)+'%');
    });
  });
})();
</script>